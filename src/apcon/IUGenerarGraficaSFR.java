    protected void setupNumberFmtObj(int k)
    {
        String patron = "0.";
        for (int j=0; j<k; j++)
            patron = patron + "#";
        if (this.numberFormatterObj instanceof DecimalFormat) {
            ((DecimalFormat) this.numberFormatterObj).applyPattern(patron);
        }
    }
  
