    public IUGenerarGraficaSFR() throws SQLException, ClassNotFoundException, FileNotFoundException {
        NumberFormat F = numberFormatterObj;
        graficaSFR = new GestorGenerarGraficaSFR();
        setupNumberFmtObj(4);
        sfr = new SFRDatos();
        results1 = graficaSFR.getProyecto(graficaSFR.getProyectoCargado());
        proy = results1.get(0);
        results = graficaSFR.getSFRDatos(graficaSFR.getProyectoCargado());

        model = new DefaultTableModel()
        { 
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex)
            {
                return false;
            }
        };

        model.addColumn("i");
        model.addColumn("d(i)");
        model.addColumn("c(i)");
        model.addColumn("c/2(i)");
        model.addColumn("n'(i)");
        model.addColumn("n(i)");
        model.addColumn("q(i)");
        model.addColumn("p(i)");
        model.addColumn("S(i)");
        model.addColumn("f(mi)");
        model.addColumn("P(mi)");
        model.addColumn("h(i)");
        model.addColumn("F(i)");
        model.addColumn("S(i)+F(i)");

        for (int i=0; i<results.size();i++)
        {
            sfr = results.get(i);

            model.addRow(new Object[] {
                sfr.getIntervalo(),
                sfr.getFallas(),
                sfr.getDC(),
                sfr.getDC2(),
                sfr.getObs(),
                sfr.getObsR(),
                F.format(sfr.getQ()).replace(",", "."),
                F.format(sfr.getP()).replace(",", "."),
                F.format(sfr.getSi()).replace(",", "."),
                F.format(sfr.getFmi()).replace(",", "."),
                F.format(sfr.getPmi()).replace(",", "."),
                F.format(sfr.getHi()).replace(",", "."),
                F.format(sfr.getFi()).replace(",", "."),
                sfr.getSiFi()});
        }

        initComponents();
        jLabelGrafica.setIcon(null);
        grafica = new ImageIcon();
    }

