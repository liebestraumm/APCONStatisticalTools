package apcon;

public class LeyNumerica
{

    double a,b;

    public LeyNumerica(double A, double B)
    {
        setA(A);
        setB(B);
    }

    public void setA(double A)
    {
        a = A;
    }

    public double getA()
    {
        return a;
    }

    public void setB(double B)
    {
        b = B;
    }

    public double getB()
    {
        return b;
    }
}